     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $setting = Setting::where('id', $id)->firstOrFail();

        if ($request->min) {
            $setting->min=$request->min;
        }
        if ($request->max) {
            $setting->max=$request->max;
        }
        if ($request->time_limit) {
            $setting->time_limit=$request->time_limit;
        }
        if ($request->status == 'toggle') {
            $setting->status=($setting->status == 0)?1:0;
        }

        $setting->save();

        return response()->json([
            'success'=>'Successful.',
        ]);
    }

